				<h2 id="overview">Overview</h2>
				<p>The Cornell Eastern Music Ensemble (formerly Cornell Chinese Music Ensemble) brings together lovers of Eastern Asian music, and promotes wider awareness and deeper understanding of Asian culture. Our repertoire includes genres such as traditional Chinese, Asian popular music, film soundtracks, and even East-West fusion. The ensemble is composed of a mixture of Eastern and Western instruments.</p>
				<h2 id="history">History</h2>
	      <img src="images/CEME_History.png" alt="background" id="backgroundinfoimg" />
	      <p>Cornell Eastern Music Ensemble was started in January 2009 by Ian Chen and Angela Chiang, who first met at a Taiwanese cultural club. Ian was a violinist and a percussionist, and very interested in picking up a Chinese instrument. Angela was a violinist and a talented erhuist with many years of performance experience. Before the end of the semester, they agreed to start the group together.</p>

				<p>Over the winter break Ian went back to Taiwan, took erhu lessons, and brought back an erhu, a zhonghu, a dulcimer, and a few dizi. He also brought back Chinese music scores, and arranged tunes and folk songs for the group. With Angela as the lead instrumentalist and Ian the conductor, they recruited many more talented musicians and gave lessons on the side. Their mentor Joseph Lin (now the first violinist at Juilliard String Quartet) was very supportive; he not only provided priceless guidance but also his office as a temporary rehearsal space for the ensemble. Soon, CEME started to perform at music events and cultural shows. In 2010 they released their first album "Melodies From The Orient" following the first showcase in Barnes Auditorium.</p>

				<p>Since then, CEME has upheld the tradition of bringing Asian music to Cornell. Its repertoire has expanded to include not only traditional Chinese music, but also Asian pop, movie soundtracks, Japanese anime music, and video-game soundtracks as well. The name of the ensemble has changed over the years from the Cornell Chinese Instrument Ensemble, to the Cornell Chinese Music Ensemble, and to finally the Cornell Eastern Music Ensemble. However, the ensemble's mission to bring Chinese music to Cornell has remained constant.</p>
