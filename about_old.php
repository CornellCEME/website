            <h3>Background Information</h3>
             <img src="images/DSC_8866s.jpg" alt="background" id="backgroundinfoimg" />
            <p>The Cornell Eastern Music Ensemble (formerly Cornell Chinese Music Ensemble) brings together lovers of Eastern Asian music, and promotes wider awareness and deeper understanding of Asian culture. Our repertoire includes genres such as traditional Chinese, Asian popular music, film soundtracks, and even East-West fusion, while the ensemble is composed of a mixture of Eastern and Western instruments.</p>
            <p>Cornell Eastern Music Ensemble was started in January 2009 by Ian Chen and Angela Chiang, who first met at a Taiwanese cultural club, on a mahjong table. Ian was a violinist and a percussionist, and at the time was very much interested in picking up a Chinese instrument. Upon the realization that a Chinese music ensemble didn't already exist at Cornell, Ian decided to start one himself, but knew that he would not be able to do this alone. Angela was a violinist, and also a very talented erhuist with many years of performance experience. Before the end of the semester, they agreed to start the group together.</p>

<p>Over the winter break Ian went back to Taiwan, took erhu lessons, and brought back with him an erhu, a zhonghu, a dulcimer, and a few dizi. He also brought back Chinese music scores, and arranged tunes and folk songs for the group. With Angela as the lead instrumentalist and Ian the conductor, they recruited many more talented musicians, and gave lessons on the side. Their mentor Joseph Lin (now the first violinist at Juilliard String Quartet) was very supportive, and not only provided priceless guidance but also his office as a temporary rehearsal space for the ensemble. Soon, CEME started to perform at music events and cultural shows, and in 2010 released their first album "Melodies From The Orient" following the first of their annual showcases in Barnes Auditorium.</p>

<p>Since then, CEME has upheld the tradition of bringing Asian music to Cornell. Its repertoire has expanded to include not only traditional Chinese music, but also Asian pop, movie soundtracks, Japanese anime music, and video-game soundtracks as well. The name of the ensemble has changed over the years from the Cornell Chinese Instrument Ensemble, to the Cornell Chinese Music Ensemble, and finally, to the Cornell Eastern Music Ensemble, but the ensemble's mission to bring Chinese music to Cornell is always constant. If you are interested in learning more about the ensemble, feel free to reach out to us!</p>
           
            
